void	mult_matrix(int m1[4], float m2[15], int res[4])
{
	res[0] = m1[0] * m2[0] + m1[1] * m2[1] + m1[2] * m2[2] + m1[3] * m2[3];
	res[1] = m1[0] * m2[4] + m1[1] * m2[5] + m1[2] * m2[6] + m1[3] * m2[7];
	res[2] = m1[0] * m2[8] + m1[1] * m2[9] + m1[2] * m2[10] + m1[3] * m2[11];
	res[3] = m1[0] * m2[12] + m1[1] * m2[13] + m1[2] * m2[14] + m1[3] * m2[15];
}
